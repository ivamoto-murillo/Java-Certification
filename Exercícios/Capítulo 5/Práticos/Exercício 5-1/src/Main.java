public class Main {

	public static void main(String[] args) {
		char c = 'b';
		
		switch (c) {
			case 'z': {
				System.out.println("É a letra z!");
				break;
			}
			case 'm': {
				System.out.println("É a letra m!");
				break;
			}
			default: {
				System.out.println("Default maroto!");
				break;
			}
			case 'a': {
				System.out.println("É a letra a!");
				break;
			}
		}
	}

}
